function set_menu() {
  for (var i = 0; i < menu_list.length; i++) {
    rowItem =
      "<tr  id =' " +
      menu_list[i] +
      "  onclick = upload_menu(id)>" +
      "<td >" +
      menu_list[i] +
      "</td> </tr>";

    $("#initial").append(rowItem);
  }
}

function upload_menu() {
  database
    .collection("menus")
    .get()
    .then((querySnapshot) => {
      querySnapshot.forEach((doc) => {
        const menu = { id: doc.id, ...doc.data() };
        const pathRef = storage.ref(`${menu.menu}.jpg`);
        pathRef.getDownloadURL().then((url) => {
          database.collection("menus").doc(menu.id).update({ image: url });
        });
      });
    });
}

